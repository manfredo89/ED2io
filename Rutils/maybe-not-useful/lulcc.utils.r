dist.tagger <<- function(x){
   sel = ! substring(x,4,5) %in% "00"
   if (any(sel)){
      ans = paste(x[sel],collapse="_")
   }else{
      ans = "UKN00"
   }#end if
   return(ans)
}#end dist.tagger
